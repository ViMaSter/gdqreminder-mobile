import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";

const API_BASE = "https://tracker.gamesdonequick.com/tracker/api/v2";
const OUTPUT_FILE = resolve(process.cwd(), "src/generated/seededEvents.ts");

const isLikelyGDQEvent = (event) => {
  const short = String(event?.short ?? "").toLowerCase();
  return short.includes("gdq") && !short.includes("cgdq");
};

const pickThreeRelevantEvents = (events, now = new Date()) => {
  const nowMs = now.getTime();
  const withTs = events
    .map((event) => ({ event, ts: new Date(event.datetime).getTime() }))
    .filter(({ ts }) => !Number.isNaN(ts));

  const past = withTs
    .filter(({ ts }) => ts <= nowMs)
    .sort((a, b) => b.ts - a.ts)
    .slice(0, 2);

  const future = withTs
    .filter(({ ts }) => ts > nowMs)
    .sort((a, b) => a.ts - b.ts)
    .slice(0, 1);

  const selected = [...past, ...future]
    .sort((a, b) => b.ts - a.ts)
    .map(({ event }) => event);

  if (selected.length >= 3) {
    return selected.slice(0, 3);
  }

  const selectedIDs = new Set(selected.map((event) => event.id));
  const fallback = withTs
    .sort((a, b) => b.ts - a.ts)
    .map(({ event }) => event)
    .filter((event) => !selectedIDs.has(event.id));

  return [...selected, ...fallback].slice(0, 3);
};

const toRunShape = (run) => ({
  id: run.id,
  name: run.name,
  display_name: run.display_name,
  console: run.console,
  category: run.category,
  starttime: run.starttime,
  endtime: run.endtime,
  video_links: Array.isArray(run.video_links) ? run.video_links : [],
  runners: Array.isArray(run.runners) ? run.runners : [],
});

const readExistingSeed = async () => {
  try {
    const content = await readFile(OUTPUT_FILE, "utf8");
    const marker = "export const seededEventsSnapshot = ";
    const start = content.indexOf(marker);
    if (start < 0) {
      return null;
    }
    const afterMarker = content.slice(start + marker.length);
    const end = afterMarker.indexOf(" as const;");
    if (end < 0) {
      return null;
    }
    return JSON.parse(afterMarker.slice(0, end));
  } catch {
    return null;
  }
};

const fetchJSON = async (url) => {
  const response = await fetch(url, {
    headers: {
      "User-Agent": "GDQReminderClient/build-seed",
    },
  });
  if (!response.ok) {
    throw new Error(`HTTP ${response.status} from ${url}`);
  }
  return response.json();
};

const generate = async () => {
  console.log("[seed] Fetching events...");
  const eventsResponse = await fetchJSON(`${API_BASE}/events/`);
  const allEvents = Array.isArray(eventsResponse?.results) ? eventsResponse.results : [];

  const filteredEvents = allEvents
    .filter(isLikelyGDQEvent)
    .map((event) => ({
      id: event.id,
      short: event.short,
      datetime: event.datetime,
    }));

  const selectedEvents = pickThreeRelevantEvents(filteredEvents);
  const runsByEventID = {};

  for (const event of selectedEvents) {
    console.log(`[seed] Fetching runs for ${event.short} (${event.id})...`);
    try {
      const runsResponse = await fetchJSON(`${API_BASE}/events/${event.id}/runs/`);
      const runs = Array.isArray(runsResponse?.results) ? runsResponse.results : [];
      runsByEventID[event.id] = runs.map(toRunShape);
    } catch (error) {
      console.warn(`[seed] Failed to fetch runs for ${event.short} (${event.id}): ${String(error)}`);
      runsByEventID[event.id] = [];
    }
  }

  const seed = {
    generatedAt: new Date().toISOString(),
    events: selectedEvents,
    runsByEventID,
  };

  const content = `// Auto-generated by scripts/generate-seeded-events.mjs\n` +
    `// eslint-disable\n` +
    `export const seededEventsSnapshot = ${JSON.stringify(seed, null, 2)} as const;\n`;

  await mkdir(dirname(OUTPUT_FILE), { recursive: true });
  await writeFile(OUTPUT_FILE, content, "utf8");
  console.log(`[seed] Wrote ${OUTPUT_FILE}`);
};

const main = async () => {
  try {
    await generate();
  } catch (error) {
    console.warn(`[seed] Seed generation failed: ${String(error)}`);
    const existing = await readExistingSeed();
    if (existing) {
      console.warn("[seed] Keeping existing seededEvents.ts");
      return;
    }

    const fallback = {
      generatedAt: new Date().toISOString(),
      events: [],
      runsByEventID: {},
    };
    const content = `// Auto-generated fallback because seed fetch failed\n` +
      `// eslint-disable\n` +
      `export const seededEventsSnapshot = ${JSON.stringify(fallback, null, 2)} as const;\n`;
    await mkdir(dirname(OUTPUT_FILE), { recursive: true });
    await writeFile(OUTPUT_FILE, content, "utf8");
    console.warn("[seed] Wrote fallback seededEvents.ts");
  }
};

await main();
